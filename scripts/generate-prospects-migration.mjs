// Reads scripts/rgv-stylists.csv and writes a Supabase migration that
// bulk-inserts the scraped prospects into outreach_prospects. Idempotent
// on ig_handle.
//
// Run: node scripts/generate-prospects-migration.mjs

import fs from 'node:fs';
import path from 'node:path';

const CSV_PATH = new URL('rgv-stylists.csv', import.meta.url);
const OUT_PATH = new URL('../supabase/migrations/20260511000004_outreach_prospects_seed.sql', import.meta.url);

function parseCsv(text) {
  const out = [];
  let row = [], cur = '', inQ = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (c === '"' && text[i + 1] === '"' && inQ) { cur += '"'; i++; continue; }
    if (c === '"') { inQ = !inQ; continue; }
    if (c === ',' && !inQ) { row.push(cur); cur = ''; continue; }
    if ((c === '\n' || c === '\r') && !inQ) {
      if (c === '\r' && text[i + 1] === '\n') i++;
      row.push(cur); cur = '';
      if (row.length > 1 || row[0] !== '') out.push(row);
      row = [];
      continue;
    }
    cur += c;
  }
  if (cur || row.length) { row.push(cur); out.push(row); }
  return out;
}

const csv = fs.readFileSync(CSV_PATH, 'utf8');
const rows = parseCsv(csv);
const header = rows.shift();
const idx = Object.fromEntries(header.map((h, i) => [h, i]));

const records = rows.map((r) => ({
  ig_handle: (r[idx.username] || '').trim().toLowerCase(),
  full_name: r[idx.full_name] || null,
  follower_count: r[idx.follower_count] ? Number(r[idx.follower_count]) : null,
  bio: r[idx.bio] || null,
  service_guess: r[idx.service_guess] || null,
  language_guess: r[idx.language_guess] || 'en',
  rgv_match: r[idx.rgv_match] === '1',
  external_url: r[idx.external_url] || null,
  is_business: r[idx.is_business] === '1',
  verified: r[idx.verified] === '1',
  source_hashtags: r[idx.source_hashtags] || null,
  sample_location: r[idx.sample_location] || null,
})).filter((r) => r.ig_handle && /^[a-z0-9._]{2,60}$/.test(r.ig_handle));

// Postgres-safe escape for a JSON literal inside a SQL string. We embed
// JSONB as $$tagged$$ to skip the single-quote dance.
const jsonText = JSON.stringify(records);

const sql = `-- 20260511000004_outreach_prospects_seed.sql
--
-- Bulk seed: ${records.length} RGV beauty pros scraped via
-- scripts/scrape-rgv-stylists.mjs. Regenerated by running
-- \`node scripts/generate-prospects-migration.mjs\` (idempotent).

insert into public.outreach_prospects (
  ig_handle, full_name, follower_count, bio,
  service_guess, language_guess, rgv_match,
  external_url, is_business, verified,
  source_hashtags, sample_location
)
select
  lower(r->>'ig_handle'),
  nullif(r->>'full_name', ''),
  nullif(r->>'follower_count', '')::integer,
  nullif(r->>'bio', ''),
  nullif(r->>'service_guess', ''),
  coalesce(nullif(r->>'language_guess', ''), 'en'),
  coalesce((r->>'rgv_match')::boolean, false),
  nullif(r->>'external_url', ''),
  coalesce((r->>'is_business')::boolean, false),
  coalesce((r->>'verified')::boolean, false),
  nullif(r->>'source_hashtags', ''),
  nullif(r->>'sample_location', '')
from jsonb_array_elements($prospects$${jsonText}$prospects$::jsonb) as r
on conflict ((lower(ig_handle))) do nothing;
`;

fs.writeFileSync(OUT_PATH, sql);
console.log(`✓ Wrote ${records.length} prospects to ${OUT_PATH.pathname}`);
console.log(`  Run: supabase db push`);
