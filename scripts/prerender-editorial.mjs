// Prerender the editorial routes to static HTML.
//
// Glossi is a client-rendered Vite SPA — the browser paints everything
// from JS. Most AI answer-engine crawlers (Perplexity, GPTBot, etc.)
// and many search crawlers fetch RAW HTML and don't run JS, so an
// article route would look like an empty <div id="root"> to them.
//
// This script runs AFTER `vite build`. It takes the built dist/index.html
// (which already has the correct hashed asset links) and, for each
// editorial route, bakes real semantic HTML into #root plus proper
// <head> metadata + JSON-LD. Crawlers read the baked content; real
// browsers load the JS and React's createRoot() cleanly replaces #root.
//
// Vercel serves filesystem matches BEFORE applying the SPA rewrite, so
// dist/editorial/<id>/index.html is returned for /editorial/<id>.
//
// New articles (e.g. ones generated by Bylined and added to GUIDES /
// GUIDE_BODIES) are picked up automatically on the next build.

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

import { GUIDES } from '../src/ios/data.js';
import { GUIDE_BODIES } from '../src/ios/guideBodies.js';

// Canonical origin. glossi.cc 307-redirects to www.glossi.cc, so the
// canonical/og URLs must use www — pointing them at a redirecting URL
// hurts SEO/AEO.
const SITE_ORIGIN = 'https://www.glossi.cc';
const OG_IMAGE = `${SITE_ORIGIN}/icon-512.png`;
// English only — language is a runtime store toggle, not a URL, so
// there is no distinct /es route to prerender. English is also the
// target for the AI-citation pilot.
const LANG = 'en';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DIST = join(__dirname, '..', 'dist');

const esc = (s = '') =>
  String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');

const escAttr = (s = '') => esc(s).replace(/"/g, '&quot;');

// JSON-LD goes inside <script>, so neutralize any "</script>" breakout.
const jsonLd = (obj) =>
  JSON.stringify(obj).replace(/</g, '\\u003c');

// ── section renderer ────────────────────────────────────────────────
// A section is one of: { h?, p }, { h?, list:[[term,def]...] },
// { h?, table:[[...row]...] }, { pull }, or { q, a } (FAQ pair —
// forward-compatible with Bylined-generated articles).
function renderSection(s) {
  const out = [];
  if (s.h) out.push(`<h2>${esc(s.h)}</h2>`);
  if (s.p) out.push(`<p>${esc(s.p)}</p>`);
  if (s.list) {
    out.push('<dl>');
    for (const [term, def] of s.list) {
      out.push(`<dt>${esc(term)}</dt><dd>${esc(def)}</dd>`);
    }
    out.push('</dl>');
  }
  if (s.bullets) {
    out.push('<ul>');
    for (const b of s.bullets) out.push(`<li>${esc(b)}</li>`);
    out.push('</ul>');
  }
  if (s.table) {
    const [head, ...rows] = s.table;
    out.push('<table><thead><tr>');
    for (const c of head) out.push(`<th>${esc(c)}</th>`);
    out.push('</tr></thead><tbody>');
    for (const r of rows) {
      out.push('<tr>');
      for (const c of r) out.push(`<td>${esc(c)}</td>`);
      out.push('</tr>');
    }
    out.push('</tbody></table>');
  }
  if (s.pull) out.push(`<blockquote>${esc(s.pull)}</blockquote>`);
  if (s.q && s.a) out.push(`<h3>${esc(s.q)}</h3><p>${esc(s.a)}</p>`);
  return out.join('\n');
}

const isOrgAuthor = (name = '') =>
  /editors|desk|team|glossi/i.test(name);

// ── shared <head> + #root injection into the built template ─────────
function compose(template, { title, description, canonical, headExtra, body }) {
  let html = template;
  html = html.replace(
    /<title>[\s\S]*?<\/title>/,
    `<title>${esc(title)}</title>`
  );
  html = html.replace(
    /<meta name="description"[^>]*>/,
    `<meta name="description" content="${escAttr(description)}">`
  );
  // Drop the homepage-flavored og/twitter tags; we inject per-page ones.
  html = html.replace(/\s*<meta property="og:[^>]*>/g, '');
  html = html.replace(/\s*<meta name="twitter:[^>]*>/g, '');

  const head = [
    `<link rel="canonical" href="${escAttr(canonical)}">`,
    `<meta property="og:title" content="${escAttr(title)}">`,
    `<meta property="og:description" content="${escAttr(description)}">`,
    `<meta property="og:url" content="${escAttr(canonical)}">`,
    `<meta property="og:image" content="${escAttr(OG_IMAGE)}">`,
    `<meta property="og:type" content="article">`,
    `<meta name="twitter:card" content="summary_large_image">`,
    `<meta name="twitter:title" content="${escAttr(title)}">`,
    `<meta name="twitter:description" content="${escAttr(description)}">`,
    `<meta name="twitter:image" content="${escAttr(OG_IMAGE)}">`,
    headExtra,
  ].join('\n');

  html = html.replace('</head>', `${head}\n</head>`);
  html = html.replace(
    '<div id="root"></div>',
    `<div id="root">${body}</div>`
  );
  return html;
}

