// ── article pages ───────────────────────────────────────────────────
function buildArticle(idx, guide, tmpl) {
  const body = GUIDE_BODIES[idx]?.[LANG];
  if (!body) return null;
  const title = guide[`t_${LANG}`];
  const kicker = guide[`kicker_${LANG}`] || '';
  const canonical = `${SITE_ORIGIN}/editorial/${idx}`;

  const sectionsHtml = (body.sections || []).map(renderSection).join('\n');
  const faqPairs = (body.sections || []).filter((s) => s.q && s.a);

  const articleHtml = `${PRERENDER_STYLE}
<main class="gprx"><article>
<p class="gprx-kicker">${esc(kicker)}</p>
<h1>${esc(title)}</h1>
<p class="gprx-dek">${esc(body.dek || '')}</p>
<p class="gprx-byline">By ${esc(body.author || '')}${
    body.authorRole ? ' &middot; ' + esc(body.authorRole) : ''
  }</p>
${sectionsHtml}
</article>
<p class="gprx-back"><a href="/editorial">&larr; All guides</a></p>
</main>`;

  const blogPosting = {
    '@context': 'https://schema.org',
    '@type': 'BlogPosting',
    headline: title,
    description: body.dek || '',
    author: {
      '@type': isOrgAuthor(body.author) ? 'Organization' : 'Person',
      name: body.author || 'Glossi',
    },
    publisher: {
      '@type': 'Organization',
      name: 'Glossi',
      url: SITE_ORIGIN,
      logo: { '@type': 'ImageObject', url: OG_IMAGE },
    },
    mainEntityOfPage: { '@type': 'WebPage', '@id': canonical },
    inLanguage: 'en-US',
  };

  const schemas = [
    `<script type="application/ld+json">${jsonLd(blogPosting)}</script>`,
  ];
  if (faqPairs.length) {
    schemas.push(
      `<script type="application/ld+json">${jsonLd({
        '@context': 'https://schema.org',
        '@type': 'FAQPage',
        mainEntity: faqPairs.map((s) => ({
          '@type': 'Question',
          name: s.q,
          acceptedAnswer: { '@type': 'Answer', text: s.a },
        })),
      })}</script>`
    );
  }

  return compose(tmpl, {
    title: `${title} — Glossi`,
    description: body.dek || title,
    canonical,
    headExtra: schemas.join('\n'),
    body: articleHtml,
  });
}

// ── editorial listing page ──────────────────────────────────────────
function buildListing(tmpl) {
  const canonical = `${SITE_ORIGIN}/editorial`;
  const items = GUIDES.map((g, i) => {
    const b = GUIDE_BODIES[i]?.[LANG];
    return `<li>
<p class="gprx-kicker">${esc(g[`kicker_${LANG}`] || '')}</p>
<h2><a href="/editorial/${i}">${esc(g[`t_${LANG}`])}</a></h2>
<p class="gprx-dek">${esc(b?.dek || '')}</p>
</li>`;
  }).join('\n');

  const body = `${PRERENDER_STYLE}
<main class="gprx">
<p class="gprx-kicker">The Glossi Field Guide</p>
<h1>Texas beauty, by neighborhood.</h1>
<p class="gprx-dek">Pricing data, salon picks, and how-to-talk-to-your-colorist guides for the Rio Grande Valley — written by people in the chair.</p>
<ul class="gprx-list">
${items}
</ul>
</main>`;

  const blog = {
    '@context': 'https://schema.org',
    '@type': 'Blog',
    name: 'The Glossi Field Guide',
    description:
      'Pricing data, salon picks, and beauty guides for the Rio Grande Valley.',
    url: canonical,
    publisher: {
      '@type': 'Organization',
      name: 'Glossi',
      url: SITE_ORIGIN,
      logo: { '@type': 'ImageObject', url: OG_IMAGE },
    },
    blogPost: GUIDES.map((g, i) => ({
      '@type': 'BlogPosting',
      headline: g[`t_${LANG}`],
      url: `${SITE_ORIGIN}/editorial/${i}`,
    })),
  };

  return compose(tmpl, {
    title: 'The Glossi Field Guide — RGV beauty guides',
    description:
      'Pricing data, salon picks, and beauty guides for the Rio Grande Valley.',
    canonical,
    headExtra: `<script type="application/ld+json">${jsonLd(blog)}</script>`,
    body,
  });
}

// ── run ─────────────────────────────────────────────────────────────
async function main() {
  const tmplPath = join(DIST, 'index.html');
  if (!existsSync(tmplPath)) {
    console.error(
      `[prerender] ${tmplPath} not found — run \`vite build\` first.`
    );
    process.exit(1);
  }
  const tmpl = await readFile(tmplPath, 'utf8');

  await mkdir(join(DIST, 'editorial'), { recursive: true });
  await writeFile(join(DIST, 'editorial', 'index.html'), buildListing(tmpl));
  let count = 1;

  for (let i = 0; i < GUIDES.length; i++) {
    const page = buildArticle(i, GUIDES[i], tmpl);
    if (!page) {
      console.warn(`[prerender] guide ${i} has no body — skipped.`);
      continue;
    }
    await mkdir(join(DIST, 'editorial', String(i)), { recursive: true });
    await writeFile(
      join(DIST, 'editorial', String(i), 'index.html'),
      page
    );
    count++;
  }

  console.log(
    `[prerender] wrote ${count} editorial page(s) → dist/editorial/`
  );

  await writeSitemap();
}

// Sitemap covers the public/indexable surface only: home, editorial
// index + every guide, and the few marketing-ish landings. Session-only
// routes (/quotes, /salon, /inbox, /me, etc.) are walled off in
// robots.txt so we don't bother listing them.
async function writeSitemap() {
  const today = new Date().toISOString().slice(0, 10);

  // [url, changefreq, priority] — keep the editorial index + articles
  // weighted highest since they're the AI-citation target.
  const staticUrls = [
    ['/', 'weekly', '1.0'],
    ['/editorial', 'weekly', '0.9'],
    ['/pros', 'monthly', '0.7'],
    ['/cities', 'monthly', '0.6'],
    ['/help', 'yearly', '0.4'],
    ['/terms', 'yearly', '0.3'],
    ['/privacy', 'yearly', '0.3'],
  ];

  const editorialUrls = GUIDES.map((_, i) => [
    `/editorial/${i}`,
    'monthly',
    '0.8',
  ]);

  const all = [...staticUrls, ...editorialUrls];

  const xml =
    `<?xml version="1.0" encoding="UTF-8"?>\n` +
    `<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n` +
    all
      .map(
        ([path, freq, prio]) =>
          `  <url>\n` +
          `    <loc>${SITE_ORIGIN}${path}</loc>\n` +
          `    <lastmod>${today}</lastmod>\n` +
          `    <changefreq>${freq}</changefreq>\n` +
          `    <priority>${prio}</priority>\n` +
          `  </url>`
      )
      .join('\n') +
    `\n</urlset>\n`;

  await writeFile(join(DIST, 'sitemap.xml'), xml);
  console.log(`[prerender] wrote sitemap.xml → ${all.length} URLs`);
}

main().catch((err) => {
  console.error('[prerender] failed:', err);
  process.exit(1);
});
